    private void ConsultaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ConsultaMouseClicked
        UI = null;
        UI.put("control", new Color(245, 190, 131));
        UI.put("OptionPane.background", new Color(245, 190, 131));
        txt_buscar = valueOf(JOptionPane.showInputDialog(null, new JLabel("Código ID: ", icono("/iconos/consu.png", 40, 40), JLabel.LEADING), "Búsqueda", JOptionPane.PLAIN_MESSAGE));
        try {  
            //Class.forName("com.mysql.jdbc.Driver");
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/bd_bank", "root", "");
            PreparedStatement pst = cn.prepareStatement("select * from sistema where ID = ?");
            pst.setString(1, txt_buscar.trim());
            ResultSet rs = pst.executeQuery();
            if (rs.next()) {
                dinero = Float.parseFloat(rs.getString("TMonto"));
                T1.setText(rs.getString("Nombre"));
                T1.setForeground(Color.BLACK);
                Ddepo.setText(rs.getString("UDeposito"));
                Ddepo.setForeground(Color.BLACK);
                Dreti.setText(rs.getString("Uretiro"));
                Dreti.setForeground(Color.BLACK);
                DMonto.setText(rs.getString("TMonto"));
                DMonto.setForeground(Color.BLACK);
                mostrar.setText("Usuario: " + rs.getString("Nombre") + "\n"
                        + "Último Monto Total: " + rs.getString("TMonto") + "\n"
                        + "Último Depósito: " + rs.getString("UDeposito") + "\n"
                        + "Último Retiro: " + rs.getString("URetiro"));
            } else {
                T1.setText("");
                Ddepo.setText("");
                Dreti.setText("");
                mostrar.setText("");
                DMonto.setText("");
                mostrar.setText("No se encuentra en la base de datos");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error", "Error", JOptionPane.PLAIN_MESSAGE, icono("/iconos/error.png", 40, 40));

        }
    }//GEN-LAST:event_ConsultaMouseClicked

