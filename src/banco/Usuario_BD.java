    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        UI = null;
        UI.put("control", new Color(245, 190, 131));
        UI.put("OptionPane.background", new Color(245, 190, 131));
        
        Document documento = new Document();
        
        try{
            String ruta = System.getProperty("user.home");
            PdfWriter.getInstance(documento, new FileOutputStream(ruta + "/Desktop/Reporte_Clientes.pdf"));
            documento.open();
            
            PdfPTable tabla = new PdfPTable(5);
            tabla.addCell("Código");
            tabla.addCell("Nombre");
            tabla.addCell("Monto Total");
            tabla.addCell("Último Depósito");
            tabla.addCell("Último Retiro");
            
            try {
                Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/bd_bank", "root", "");
                PreparedStatement pst = cn.prepareStatement("select * from sistema");
                
                ResultSet rs = pst.executeQuery();
                
                if(rs.next()){
                    
                    do {
                        tabla.addCell(rs.getString(1));
                        tabla.addCell(rs.getString(2));
                        tabla.addCell(rs.getString(3));
                        tabla.addCell(rs.getString(4));
                        tabla.addCell(rs.getString(5));
                    } while(rs.next());
                    documento.add(tabla);
                }
            }catch(DocumentException | SQLException e){    
            }
            
            documento.close();
            JOptionPane.showMessageDialog(null, "Reporte creado.", "PDF", JOptionPane.PLAIN_MESSAGE, icono("/iconos/abejita.png", 40, 40));
        
        }catch(DocumentException | HeadlessException | FileNotFoundException e){
            
        } 
    }//GEN-LAST:event_jButton1ActionPerformed
